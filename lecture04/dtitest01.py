W = float(input("โปรดใส่ความกว้างของกล่อง : "))
H = float(input("โปรดใส่ความสูงของกล่อง : "))
L = float(input("โปรดใส่ความยาวของกล่อง : "))
print("---------------------------------------")

Area = round((2 * (L * W+ W* H + H * L))/5)
print(f"ค่าสี่เหลี่ยม {W} {H} {L} จะต้องใช้สี {Area}")
print("ค่าสี่เหลี่ยม {} {} {} จะต้องใช้สี {}".format(W,H,L,Area))
print("ค่าสี่เหลี่ยม" ,W,H, L, "จะต้องใช้สี",Area,)
print(str("ค่าสี่เหลี่ยม")+ str(W) +str(H) +str(L) +str("จะต้องใช้สี") +str(Area))